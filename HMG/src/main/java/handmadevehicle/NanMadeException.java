package handmadevehicle;

public class NanMadeException extends RuntimeException {

	public NanMadeException()
	{
		super();
	}

	public NanMadeException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public NanMadeException(String message)
	{
		super(message);
	}

	public NanMadeException(Throwable cause)
	{
		super(cause);
	}

}